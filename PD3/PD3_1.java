package PD3;

public class PD3_1 {
    public static void main(String[] args) {
        byte myNumber = 58; // 1B
        System.out.println(myNumber);
        short myShort = 66; // 2B
        System.out.println(myShort);
        int myInt = 74; // 4B
        System.out.println(myInt);
        long myLong = 6592L;
        System.out.println(myLong);
        float myFloat = 9.6f;
        System.out.println(myFloat);
        double myDouble = 8.2469; // 8
    }
}
